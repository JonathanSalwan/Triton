#ifndef TRITON_CPUSIZE_H
#define TRITON_CPUSIZE_H

/*! Returns the FLAG's size in bit. */
#define FLAG_SIZE_BIT     1

/*! Returns the BYTE's size in byte. */
#define BYTE_SIZE         1

/*! Returns the BYTE's size in bit. */
#define BYTE_SIZE_BIT     8

/*! Returns the WORD's size in byte. */
#define WORD_SIZE         2

/*! Returns the WORD's size in bit. */
#define WORD_SIZE_BIT     16

/*! Returns the DWORD's size in byte. */
#define DWORD_SIZE        4

/*! Returns the DWORD's size in bit. */
#define DWORD_SIZE_BIT    32

/*! Returns the QWORD's size in byte. */
#define QWORD_SIZE        8

/*! Returns the QWORD's size in bit. */
#define QWORD_SIZE_BIT    64

/*! Returns the DQWORD's size in byte. */
#define DQWORD_SIZE       16

/*! Returns the DQWORD's size in bit. */
#define DQWORD_SIZE_BIT   128

#endif /* TRITON_CPUSIZE_H */

